/******************************************************************************

Ex.25
 Faça um programa, utilizando while, que mostre na tela os números de 0 a 100.

*******************************************************************************/
public class ex25
{
	public static void main(String[] args) {
		int n = 1;
		
		while (n < 100){
	     n++;
		
		System.out.println(+n);
		
		}
	}
}